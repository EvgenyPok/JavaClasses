package com.class7;

public class CompoundOperatorsPractice {
public static void main(String[] args) {
	//1.
	int a = 50;
	a+=100;
	System.out.println(a);//150
	//2.
	int b=100;
	b-=67;
	System.out.println(b);//33
	//3.
	double cakePiece=11;
	cakePiece/=4;
	System.out.println(cakePiece);//11div4
	//4.
	cakePiece=25;
	cakePiece%=7;
	System.out.println(cakePiece);
}
}
