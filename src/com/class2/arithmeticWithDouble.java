package com.class2;

public class arithmeticWithDouble {
public static void main(String[] args) {
	String name="Jimmy";
	double a=77.55d;
	double b=653.32d;
	double sum=a+b;
	double dif=a-b;
	double mult= a*b;
	double div = a/b;
	
	System.out.println("The addition of 2 numbers "+a+" and "+b+" is "+sum);
	System.out.println("The difference of 2 numbers "+a+" and "+b+" is "+dif);
	System.out.println("The multiplication of 2 numbers "+a+" and "+b+" is "+mult);
	System.out.println("The division of 2 numbers " +a+" and "+b+" is "+div);
	System.out.println(name.length);
}
}
