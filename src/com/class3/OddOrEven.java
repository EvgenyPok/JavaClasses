package com.class3;

public class OddOrEven {

	public static void main(String[] args) {
		int a=96867;
		if (a%2==0) {
			System.out.println("The number "+a+" is EVEN");
		}else if (a==0) {
			System.out.println("The number is 0");
		}else {
			System.out.println("The number "+a+" is ODD");
		}

	}

}
